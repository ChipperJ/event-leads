// Hosted Supabase + GitHub secrets setup. Run: npm run setup:hosted
"use strict";

const fs = require("fs");
const path = require("path");
const readline = require("readline");
const { execFileSync, spawnSync } = require("child_process");

const rl = readline.createInterface({ input: process.stdin, output: process.stdout });

function question(prompt) {
  return new Promise((resolve) => {
    rl.question(prompt, resolve);
  });
}

function getGitHubRepo() {
  try {
    return execFileSync("gh", ["repo", "view", "--json", "nameWithOwner", "-q", ".nameWithOwner"], {
      encoding: "utf8",
    }).trim();
  } catch {
    console.error("Need GitHub CLI: install from https://cli.github.com then run: gh auth login");
    process.exit(1);
  }
}

function ghSecretSet(name, value, repo) {
  const r = spawnSync("gh", ["secret", "set", name, "--repo", repo, "--body", value], {
    encoding: "utf8",
    stdio: ["pipe", "pipe", "pipe"],
  });
  if (r.status !== 0) {
    console.error(r.stderr || r.stdout || "gh secret set failed");
    process.exit(r.status || 1);
  }
}

async function main() {
  console.log("\n=== Event Leads - hosted Supabase setup ===\n");
  console.log("Paste values from Supabase. Secrets go to GitHub Actions; .env.local stays on this PC.\n");

  const repo = getGitHubRepo();
  console.log("GitHub repo:", repo, "\n");

  const accessToken = await question("1) Paste Supabase access token (dashboard > account > access tokens): ");
  if (!String(accessToken).trim()) {
    console.error("Access token required.");
    process.exit(1);
  }
  ghSecretSet("SUPABASE_ACCESS_TOKEN", String(accessToken).trim(), repo);

  const projectId = await question("\n2) Paste project Reference ID (Settings > General): ");
  if (!String(projectId).trim()) {
    console.error("Project ref required.");
    process.exit(1);
  }
  const projectRef = String(projectId).trim();
  ghSecretSet("SUPABASE_PROJECT_ID", projectRef, repo);

  const dbPassword = await question("\n3) Paste database password (Settings > Database): ");
  if (!String(dbPassword)) {
    console.error("Database password required.");
    process.exit(1);
  }
  ghSecretSet("SUPABASE_DB_PASSWORD", String(dbPassword), repo);

  console.log("\nStarting GitHub workflow: Deploy Supabase migrations ...");
  spawnSync("gh", ["workflow", "run", "Deploy Supabase migrations", "--repo", repo], {
    stdio: "inherit",
    windowsHide: true,
  });

  const url = await question("\n4) Paste project URL (https://YOUR_REF.supabase.co): ");
  const anon = await question("5) Paste anon public key (NOT service_role): ");

  const repoRoot = path.resolve(__dirname, "..");
  if (String(url).trim() && String(anon).trim()) {
    if (!fs.existsSync(path.join(repoRoot, ".env.example"))) {
      console.error("Missing .env.example - run this from the event-leads repo root.");
      process.exit(1);
    }
    const nl = "\n";
    const body =
      "# Generated by configure-hosted-supabase - do not commit" +
      nl +
      `NEXT_PUBLIC_SUPABASE_URL=${String(url).trim()}` +
      nl +
      `NEXT_PUBLIC_SUPABASE_ANON_KEY=${String(anon).trim()}` +
      nl +
      nl +
      "ANTHROPIC_API_KEY=" +
      nl +
      "OPENAI_API_KEY=" +
      nl +
      "RESEND_API_KEY=" +
      nl +
      "FROM_EMAIL=" +
      nl +
      "TWILIO_ACCOUNT_SID=" +
      nl +
      "TWILIO_AUTH_TOKEN=" +
      nl +
      "TWILIO_PHONE_NUMBER=" +
      nl;
    const envLocal = path.join(repoRoot, ".env.local");
    fs.writeFileSync(envLocal, body, "utf8");
    console.log("\nWrote", envLocal);
  } else {
    console.warn("\nSkipping .env.local - copy .env.example to .env.local and fill by hand.");
  }

  console.log("\n=== Done ===");
  console.log("Watch the run: gh run watch --repo", repo);
  console.log("(or GitHub.com > your repo > Actions)\n");

  const authUrl = `https://supabase.com/dashboard/project/${projectRef}/auth/url-configuration`;
  try {
    if (process.platform === "win32") {
      spawnSync("cmd", ["/c", "start", "", authUrl], { stdio: "ignore", windowsHide: true });
    } else {
      spawnSync("open", [authUrl], { stdio: "ignore" });
    }
    console.log("Opened Supabase Auth URL settings in your browser.");
    console.log("Set Site URL http://localhost:3000 and add redirect http://localhost:3000/auth/callback if needed.\n");
  } catch {
    console.log("In Supabase: Authentication > URL Configuration - add http://localhost:3000/auth/callback\n");
  }

  console.log("Then run: npm run dev\n");
  rl.close();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
